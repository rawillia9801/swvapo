create table if not exists public.breeding_dogs (
  id bigint generated by default as identity primary key,
  role text not null check (role in ('dam', 'sire')),
  display_name text not null,
  registered_name text,
  call_name text,
  status text not null default 'active',
  date_of_birth date,
  color text,
  coat_type text,
  registration_no text,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.litters (
  id bigint generated by default as identity primary key,
  litter_code text not null,
  litter_name text,
  dam_id bigint references public.breeding_dogs(id) on delete restrict,
  sire_id bigint references public.breeding_dogs(id) on delete restrict,
  whelp_date date,
  status text not null default 'planned',
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists litters_litter_code_key on public.litters (litter_code);
create index if not exists litters_dam_id_idx on public.litters (dam_id);
create index if not exists litters_sire_id_idx on public.litters (sire_id);

